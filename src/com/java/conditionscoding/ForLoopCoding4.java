package com.java.conditionscoding;

import java.util.*;

public class ForLoopCoding4 {

	public static void main(String[] args) {
		ArrayList<String> a = new ArrayList<String>();
		a.add("Rkae");
		a.add("sahu");

		for (int i = 0; i < a.size(); i++) {
			System.out.println(a.get(i));
		}
	}
}
