package com.java.conceptjavaclass;

public class InstanceVariableProcessing1 {
	static int a =5;
	static int b =6;
	static void sumMethod()
	{
		int sum =a+b;
		System.out.println("sum of the mentioned no. is "+sum);
		 
	}
	public static void main(String [] args)
	{
		System.out.println(5);
		sumMethod();
	}
}
